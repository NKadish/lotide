const findKey = function(object, callback) {
  let whichStar;
  for (const key in object) { //goes through all of the keys in the object
    if (callback(object[key]) === true) { //checks to see if the callback is true.
      whichStar = key; //when the callback is true whichStar becomes key
      return whichStar; //then we return it
    }
  }
  return whichStar; //if there is no star that makes the callback true, we return whichStar which is undefined. 
}

